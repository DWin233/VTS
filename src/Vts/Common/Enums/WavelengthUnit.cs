namespace Vts
{
    /// <summary>
    /// Enum to represent the possible wavelength and wavenumber inputs
    /// </summary>
    public enum WavelengthUnit
    {
        /// <summary>
        /// [nm]
        /// </summary>
        Nanometers,
        /// <summary>
        /// [um] microns
        /// </summary>
        Micrometers,
        /// <summary>
        /// [m] meters
        /// </summary>
        Meters,
        /// <summary>
        /// [1/m]
        /// </summary>
        InverseMeters,
        /// <summary>
        /// [1/cm]
        /// </summary>
        InverseCentimeters,
    }
}