namespace Vts
{
    /// <summary>
    /// plot normalization types
    /// </summary>
    public enum PlotNormalizationType
    {
        /// <summary>
        /// no plot normalization
        /// </summary>
        None,
        /// <summary>
        /// plot normalization relative to plot max
        /// </summary>
        RelativeToMax,
        /// <summary>
        /// plot normalization relative to curve
        /// </summary>
        RelativeToCurve
    }
}