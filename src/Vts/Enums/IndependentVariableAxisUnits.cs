namespace Vts
{
    /// <summary>
    /// independent variable axis unit types.  These are the default units used throughout code
    /// </summary>
    public enum IndependentVariableAxisUnits
    {
        /// <summary>
        /// millimeter (mm)
        /// </summary>
        MM,
        /// <summary>
        /// nanosecond (ns)
        /// </summary>
        NS,
        /// <summary>
        /// millimeter^(-1)
        /// </summary>
        InverseMM,
        /// <summary>
        /// giga-Hertz
        /// </summary>
        GHz,
        /// <summary>
        /// nanometers
        /// </summary>
        NM,
    }
}